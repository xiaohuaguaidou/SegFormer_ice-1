    def _load_pretrained(self, weight_path):
        try:
            print(f"Loading pretrained weights from {weight_path}")
            state_dict = torch.load(weight_path, map_location='cpu')
            model_state_dict = self.state_dict()
            filtered_state_dict = {}

            for k, v in state_dict.items():
                if k in model_state_dict and v.shape == model_state_dict[k].shape:
                    if all(x not in k for x in
                           ['integrated_attn', 'edge_attention', 'large_kernel_attn', 'fusion_gate']):
                        filtered_state_dict[k] = v

            self.load_state_dict(filtered_state_dict, strict=False)
            print(f"Loaded {len(filtered_state_dict)}/{len(state_dict)} parameters")
        except Exception as e:
            print(f"Error loading pretrained weights: {e}")


